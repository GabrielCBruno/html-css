const menu = document.getElementById('itens')

function interacao() {
    if (menu.style.display == 'none') {
        menu.style.display = 'block'
    }else {
        menu.style.display = 'none'
    }
}

function verificar() {
    if (menu.style.display == 'none') {
        menu.style.display = 'block'
    }
}